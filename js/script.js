// MODAL1
const botaoAbrir1 = document.querySelector("#abrir1");
const botaoFechar1 = document.querySelector('#fechar1');
const containerModal1 = document.querySelector("#container1");

function abrirModal1() {
    event.preventDefault();
    this
    containerModal1.classList.add('ativo');
}
function fecharModal1(event) {
    event.preventDefault();
    containerModal1.classList.remove('ativo');
}
function cliqueForaModal1(event) {
    if (event.target === this) {
        fecharModal1(event);
    }
}
botaoAbrir1.addEventListener('click', abrirModal1);
botaoFechar1.addEventListener('click', fecharModal1);
containerModal1.addEventListener('click', cliqueForaModal1);

